    private void setSelectableDateLimits() {
        Calendar cal = Calendar.getInstance();
        Date today = cal.getTime(); // Get today's date
        date_choose.setMinSelectableDate(today); // Prevent selecting past dates
        date_choose.setMaxSelectableDate(null); // Allow selection of any future date
    }

    private boolean validateParticipants() {
    String participantText = part_count.getText().trim();
    
    // Check if the input is empty
    if (participantText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the number of participants.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    
    // Check if the input is an integer
    try {
        int participants = Integer.parseInt(participantText);
        
        // Check if the number is greater than 0
        if (participants <= 0 ) {
            JOptionPane.showMessageDialog(this, "Number of participants must be greater than 0 or 1.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid integer.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    
    return true;
}

    
    private void con_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_con_btnActionPerformed
       if (!validateParticipants()) {
        return; // Exit the method if validation fails
    }
    
    String gameID = game_id.getText();
    String gameName = sports_type.getSelectedItem().toString();
    String gameVenue = venue.getSelectedItem().toString();
    Date gameDate = date_choose.getDate(); // Get date from JDateChooser
    String sportType = sports_type.getSelectedItem().toString(); // Assume you have a dropdown for this
    
    // Get and validate number of participants
    int participants = Integer.parseInt(part_count.getText().trim());

    // Validate other inputs
    if (gameID.isEmpty() || gameDate == null || sportType.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all required fields!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Format the date
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String formattedDate = sdf.format(gameDate);
    
    // Insert into the database
    Connection conn = null;
    PreparedStatement ps = null;
    try {
        // Establish database connection
        conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "system", "cristianosui7");
        
        // Prepare the SQL insert statement
        String sql = "INSERT INTO individual_games (GAME_ID, GAME_NAME, VENUE, GAME_DATE, NO_OF_PARTICIPANTS, MAX_PARTICIPANTS, SPORT_TYPE) " + 
             "VALUES (?, ?, ?, TO_DATE(?, 'YYYY-MM-DD'), ?, ?, ?)";

        ps = conn.prepareStatement(sql);
        
        // Set parameters
        ps.setString(1, gameID);
        ps.setString(2, gameName);
        ps.setString(3, gameVenue);
        ps.setString(4, formattedDate);  // Date is passed as a string
        ps.setInt(5, participants);
        ps.setInt(6, 0); // Set MAX_PARTICIPANTS to 0
        ps.setString(7, sportType);
                        
        
        // Execute the insert
        int rowsInserted = ps.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Game created successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error while creating the game: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    } finally {
        // Clean up database resources
        try {
            if (ps != null) ps.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_con_btnActionPerformed

