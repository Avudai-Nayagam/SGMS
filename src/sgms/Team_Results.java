    private void fetchAndDisplayTeamResults() {
    // Database connection details
    String DB_URL = "jdbc:oracle:thin:@localhost:1521:xe";
    String DB_USER = "system";
    String DB_PASS = "cristianosui7";

    // Get the logged-in team from the UserSession instance
    UserSession session = UserSession.getInstance();
    String loggedInTeam = session.getUsername(); // Replace with actual team name if stored differently
    System.out.println("Logged in team's username: " + loggedInTeam);

    // SQL query to fetch results for the logged-in team
    String sql = "SELECT g.VENUE, TO_CHAR(g.GAME_DATE, 'YYYY-MM-DD') AS GAME_DATE, " +
                 "CASE WHEN UPPER(gr.TEAM1) = UPPER(?) THEN gr.TEAM2 ELSE gr.TEAM1 END AS OPPONENT, " +
                 "CASE WHEN UPPER(gr.WINNER) = UPPER(?) THEN 'Win' ELSE 'Loss' END AS RESULT " +
                 "FROM game_results gr " +
                 "JOIN games_team g ON gr.GAME_ID = g.GAME_ID " +
                 "WHERE UPPER(gr.TEAM1) = UPPER(?) OR UPPER(gr.TEAM2) = UPPER(?)";

    try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS)) {
        // Prepare the SQL statement
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, loggedInTeam);  // Checking if TEAM1 is logged in team
        stmt.setString(2, loggedInTeam);  // Checking if WINNER is logged in team
        stmt.setString(3, loggedInTeam);  // Checking TEAM1 against logged in team
        stmt.setString(4, loggedInTeam);  // Checking TEAM2 against logged in team
        System.out.println("Executing query: " + stmt);

        // Execute the query
        ResultSet rs = stmt.executeQuery();

        // Prepare a StringBuilder to accumulate the results
        StringBuilder results = new StringBuilder();

        // Loop through the result set
        while (rs.next()) {
            String venue = rs.getString("VENUE");
            String gameDate = rs.getString("GAME_DATE");
            String opponent = rs.getString("OPPONENT");
            String result = rs.getString("RESULT");

            // Append each game's details to the results StringBuilder
            results.append("Game Date: ").append(gameDate)
                   .append("\nVenue: ").append(venue)
                   .append("\nOpponent: ").append(opponent)
                   .append("\nResult: ").append(result)
                   .append("\n\n");
        }

        // Check if no results were found
        if (results.length() == 0) {
            results.append("No results found for this team.");
        }

        // Display the results in the text area
        res_field.setText(results.toString());

    } catch (SQLException e) {
        e.printStackTrace();
        res_field.setText("Error retrieving team results.");
    }
}





    
