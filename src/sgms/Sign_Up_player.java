    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
    
        char[] passwordChars = pass.getPassword();
char[] confirmPasswordChars = con_pass.getPassword();
        
    String playerName = pl_name.getText();
    String username = user.getText();
    String passwordText = new String(passwordChars);
    String confirmPassword = new String(confirmPasswordChars);
    String emailText = email.getText();
    String phoneNumber = ph_no.getText();
    String ageText = age.getText();
    String sportType = (String) sports_type.getSelectedItem();

    // Validate input (you can add more validation as needed)
    if (playerName.isEmpty() || username.isEmpty() || passwordText.isEmpty() ||
            confirmPassword.isEmpty() || emailText.isEmpty() || phoneNumber.isEmpty() || ageText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!passwordText.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    Arrays.fill(passwordChars, '0');
    Arrays.fill(confirmPasswordChars, '0');

    // Validate age
    int ageValue;
    try {
        ageValue = Integer.parseInt(ageText);
        if (ageValue < 0) {
            JOptionPane.showMessageDialog(this, "Age must be a non-negative number", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Age must be a valid integer", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate email
    if (!emailText.endsWith("@gmail.com")) {
        JOptionPane.showMessageDialog(this, "Email must end with @gmail.com", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Database connection and insert logic
    try (Connection conn = DriverManager.getConnection(DB_URL, USER, PASSWORD)) {
        String sql = "INSERT INTO players (player_name, username, password, email, phone_number, age, sport_type) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, playerName);
        pstmt.setString(2, username);
        pstmt.setString(3, passwordText);
        pstmt.setString(4, emailText);
        pstmt.setString(5, phoneNumber);
        pstmt.setInt(6, ageValue); // Store age as an integer
        pstmt.setString(7, sportType);
        pstmt.executeUpdate();

        JOptionPane.showMessageDialog(this, "Registration successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
        // Optionally clear the fields after submission
        clearFields();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_submitActionPerformed

