    private void loadPlayerResults() {
    // Database connection details
    String DB_URL = "jdbc:oracle:thin:@localhost:1521:xe";
    String DB_USER = "system";
    String DB_PASS = "cristianosui7";

    // Get the current player's username from the session (this should be passed to this method)
    UserSession session = UserSession.getInstance();
    String username = session.getUsername(); // Replace with actual username if stored differently

    try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS)) {
        // SQL query to fetch the results for the player
        String sql = "SELECT ig.game_date, ig.venue, ig.sport_type, "
                   + "CASE "
                   + "WHEN igr.first = ? THEN '1st' "
                   + "WHEN igr.second = ? THEN '2nd' "
                   + "WHEN igr.third = ? THEN '3rd' "
                   + "ELSE 'Not Ranked' "
                   + "END AS position "
                   + "FROM individual_games ig "
                   + "JOIN game_participants gp ON ig.game_id = gp.game_id "
                   + "LEFT JOIN individual_game_results igr ON ig.game_id = igr.game_id "
                   + "WHERE gp.username = ?";

        // Prepare statement
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, username); // Player's username for first place check
        stmt.setString(2, username); // Player's username for second place check
        stmt.setString(3, username); // Player's username for third place check
        stmt.setString(4, username); // Player's username in the game_participants table

        ResultSet rs = stmt.executeQuery();

        // Prepare a StringBuilder to store the results
        StringBuilder results = new StringBuilder();

        // Process the result set and display in the text area
        while (rs.next()) {
            String gameDate = rs.getString("game_date");
            String venue = rs.getString("venue");
            String sportType = rs.getString("sport_type");
            String position = rs.getString("position");

            results.append("Game Date: ").append(gameDate)
                   .append("\nVenue: ").append(venue)
                   .append("\nSport: ").append(sportType)
                   .append("\nPosition: ").append(position)
                   .append("\n\n");
        }

        // If no results, inform the player
        if (results.length() == 0) {
            results.append("You haven't participated in any games or have not been ranked yet.");
        }

        // Set the results in the text area
        result_area.setText(results.toString());

    } catch (SQLException e) {
        e.printStackTrace();
        result_area.setText("Error retrieving results.");
    }
}

    
