    private void populateGames(String sportsType) {
    try {
        // Database connection details
        String DB_URL = "jdbc:oracle:thin:@localhost:1521:xe";
        String DB_USER = "system";
        String DB_PASS = "cristianosui7";

        Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);

        // Query to fetch games for the selected sports type where either TEAM1_ID or TEAM2_ID is still NULL
        String query = "SELECT GAME_ID, GAME_NAME, VENUE, TO_CHAR(GAME_DATE, 'YYYY-MM-DD') AS GAME_DATE " +
                       "FROM games_team " +
                       "WHERE GAME_NAME = ? " +  // Match the sports type
                       "AND (TEAM1_ID IS NULL OR TEAM2_ID IS NULL)"; // Exclude games with two registered teams

        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, sportsType);
        ResultSet rs = pstmt.executeQuery();

        // Clear the combo box before adding new items
        gameComboBox.removeAllItems();

        // Add available games to the combo box with venue and date details
        while (rs.next()) {
            String gameId = rs.getString("GAME_ID");
            String gameName = rs.getString("GAME_NAME");
            String venue = rs.getString("VENUE");
            String gameDate = rs.getString("GAME_DATE"); // formatted as 'YYYY-MM-DD'
            
            // Format the combo box item to include game ID, name, venue, and date
            String gameDetails = gameId + " - " + gameName + " | " + venue + " | " + gameDate;
            gameComboBox.addItem(gameDetails); // Add game details to the combo box
        }

        rs.close();
        pstmt.close();
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
}

