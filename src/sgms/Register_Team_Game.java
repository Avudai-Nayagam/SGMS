/**
 * Method to register the team for the selected game and update the TEAM1_ID or TEAM2_ID in the table.
 */
private void registerTeamForGame(String selectedGameId, String teamUsername) {
    try {
        // Database connection details
        String DB_URL = "jdbc:oracle:thin:@localhost:1521:xe";
        String DB_USER = "system";
        String DB_PASS = "cristianosui7";

        Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);

        // First, check if TEAM1_ID or TEAM2_ID is already the current team
        String query = "SELECT TEAM1_ID, TEAM2_ID FROM games_team WHERE GAME_ID = ?";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, selectedGameId);
        ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            String team1 = rs.getString("TEAM1_ID");
            String team2 = rs.getString("TEAM2_ID");

            // Check if the team is already registered as TEAM1_ID or TEAM2_ID
            if (teamUsername.equals(team1) || teamUsername.equals(team2)) {
                JOptionPane.showMessageDialog(this, "You are already registered for this game.");
                return; // Exit the method, since the team is already registered
            }

            // Prepare the update query
            String updateQuery = null;

            // If TEAM1_ID is NULL, assign the current team to TEAM1_ID
            if (team1 == null || team1.isEmpty()) {
                updateQuery = "UPDATE games_team SET TEAM1_ID = ? WHERE GAME_ID = ?";
            }
            // If TEAM1_ID is filled and TEAM2_ID is NULL, assign the current team to TEAM2_ID
            else if (team2 == null || team2.isEmpty()) {
                updateQuery = "UPDATE games_team SET TEAM2_ID = ? WHERE GAME_ID = ?";
            } else {
                // Both teams are already registered, no more slots available
                JOptionPane.showMessageDialog(this, "Sorry, this game is already full.");
                return;
            }

            // If update query is prepared, execute it
            if (updateQuery != null) {
                PreparedStatement updateStmt = conn.prepareStatement(updateQuery);
                updateStmt.setString(1, teamUsername); // Set the team username
                updateStmt.setString(2, selectedGameId); // Set the game ID

                int rowsUpdated = updateStmt.executeUpdate(); // Execute the update
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "Team registered successfully.");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to register the team.");
                }

                // Commit the transaction if auto-commit is disabled
                conn.commit();

                updateStmt.close();
            }
        }

        rs.close();
        pstmt.close();
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
}


    
