     private void loadTeamSpecificGames() {
     String loggedInUsername = UserSession.getInstance().getUsername();

        if (loggedInUsername == null || loggedInUsername.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No username found. Please log in.");
            return;
        }

        // Fetch the sports type for the logged-in user (team)
        String sportsType = getTeamSportsType(loggedInUsername);

        // Populate games only for the matching sports type
        if (sportsType != null) {
            populateGames(sportsType);
        } else {
            JOptionPane.showMessageDialog(this, "No sports type found for the team.");
        }
    }
     
